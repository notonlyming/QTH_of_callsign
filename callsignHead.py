# 获取呼号单个字符所代表的34进制数码
def get_char_code_for_callsign(a_char:str):
    a_char_unicode:int = ord(a_char)
    result:int = 0
    if 65 <= a_char_unicode < 65+26:
        result = a_char_unicode - 65
    elif 50 <= a_char_unicode <= 50 + 9 - 2:
        result = a_char_unicode - 24
    else:
        raise Exception(f"呼号字符：{a_char} 没有落在有效范围，请检查。")
    return result

# 获取呼号部分的求和值
def get_callsign_part_sum(callsign_part:str):
    head_code:int = 0
    if not(0 < len(callsign_part) <= 3):
        raise Exception(f"呼号部分的长度不应为{len(callsign_part)}，请检查。")
    for i in range(len(callsign_part)):
        head_code = head_code + get_char_code_for_callsign(callsign_part[i]) * (34**(3-i-1))
    return head_code

